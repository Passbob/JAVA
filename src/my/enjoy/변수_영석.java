package my.enjoy;

public class 변수_영석 {
    // 유니코드를 참고하여 'c' "Z"의 사칙연산을 표현해주세요
    public static void main(String[] args) {
        System.out.println("합 : "+('c'+'z'));
        System.out.println("차 : "+('c'-'z'));
        System.out.println("곱 : "+('c'*'z'));
        System.out.println("나눈 몫 : "+('c'/'z'));
        System.out.println("나머지 : "+('c'%'z'));
    }
}
